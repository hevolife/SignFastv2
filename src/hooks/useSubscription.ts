import { useState, useEffect } from 'react';
import { useAuth } from '../contexts/AuthContext';
import { useDemo } from '../contexts/DemoContext';
import { supabase } from '../lib/supabase';

export interface SubscriptionData {
  isSubscribed: boolean;
  subscriptionStatus: string | null;
  priceId: string | null;
  currentPeriodEnd: number | null;
  cancelAtPeriodEnd: boolean;
  hasSecretCode: boolean;
  secretCodeType: string | null;
  secretCodeExpiresAt: string | null;
  loading: boolean;
}

export const useSubscription = () => {
  const { user } = useAuth();
  const { isDemoMode } = useDemo();
  const [subscription, setSubscription] = useState<SubscriptionData>({
    isSubscribed: false,
    subscriptionStatus: null,
    priceId: null,
    currentPeriodEnd: null,
    cancelAtPeriodEnd: false,
    hasSecretCode: false,
    secretCodeType: null,
    secretCodeExpiresAt: null,
    loading: true,
  });

  useEffect(() => {
    if (user) {
      fetchSubscription();
    } else {
      setSubscription({
        isSubscribed: false,
        subscriptionStatus: null,
        priceId: null,
        currentPeriodEnd: null,
        cancelAtPeriodEnd: false,
        hasSecretCode: false,
        secretCodeType: null,
        secretCodeExpiresAt: null,
        loading: false,
      });
    }
  }, [user, isDemoMode]);

  const fetchSubscription = async () => {
    try {
      console.log('üîç [useSubscription] ========== D√âBUT V√âRIFICATION ==========');
      console.log('üîç [useSubscription] User ID:', user?.id);

      // En mode d√©mo, simuler un abonnement √† vie
      if (isDemoMode) {
        console.log('üé≠ [useSubscription] Mode d√©mo activ√©');
        setSubscription({
          isSubscribed: true,
          subscriptionStatus: 'active',
          priceId: null,
          currentPeriodEnd: null,
          cancelAtPeriodEnd: false,
          hasSecretCode: true,
          secretCodeType: 'lifetime',
          secretCodeExpiresAt: null,
          loading: false,
        });
        return;
      }

      // V√©rifier si Supabase est configur√©
      const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
      const supabaseKey = import.meta.env.VITE_SUPABASE_ANON_KEY;
      
      if (!supabaseUrl || !supabaseKey || supabaseUrl.includes('placeholder') || supabaseKey.includes('placeholder')) {
        console.warn('‚ö†Ô∏è [useSubscription] Supabase non configur√©');
        setSubscription({
          isSubscribed: false,
          subscriptionStatus: null,
          priceId: null,
          currentPeriodEnd: null,
          cancelAtPeriodEnd: false,
          hasSecretCode: false,
          secretCodeType: null,
          secretCodeExpiresAt: null,
          loading: false,
        });
        return;
      }

      // D√©terminer l'utilisateur cible (gestion impersonation)
      let targetUserId = user.id;
      const impersonationData = localStorage.getItem('admin_impersonation');
      if (impersonationData) {
        try {
          const data = JSON.parse(impersonationData);
          targetUserId = data.target_user_id;
          console.log('üë§ [useSubscription] Impersonation:', targetUserId);
        } catch (error) {
          console.error('‚ùå [useSubscription] Erreur parsing impersonation:', error);
        }
      }

      // üî• √âTAPE 1 : V√©rifier les codes secrets en PREMIER
      console.log('üîë [useSubscription] ========== √âTAPE 1 : CODES SECRETS ==========');
      let hasActiveSecretCode = false;
      let secretCodeType = null;
      let secretCodeExpiresAt = null;
      
      try {
        // R√©cup√©rer les codes secrets actifs de l'utilisateur
        const { data: userSecretCodes, error: secretCodesError } = await supabase
          .from('user_secret_codes')
          .select(`
            expires_at,
            activated_at,
            secret_codes!inner (
              id,
              type,
              is_active
            )
          `)
          .eq('user_id', targetUserId)
          .eq('secret_codes.is_active', true)
          .order('activated_at', { ascending: false });

        console.log('üîë [useSubscription] Requ√™te codes termin√©e');
        console.log('üîë [useSubscription] Erreur?', secretCodesError);
        console.log('üîë [useSubscription] Donn√©es brutes:', JSON.stringify(userSecretCodes, null, 2));

        if (secretCodesError) {
          console.error('‚ùå [useSubscription] Erreur r√©cup√©ration codes:', secretCodesError);
        } else if (userSecretCodes && userSecretCodes.length > 0) {
          console.log('üîë [useSubscription] Codes trouv√©s:', userSecretCodes.length);
          
          // V√©rifier chaque code actif
          for (const codeData of userSecretCodes) {
            const secretCode = codeData.secret_codes;
            
            console.log('üîë [useSubscription] Analyse code:', {
              codeData: JSON.stringify(codeData, null, 2),
              secretCode: JSON.stringify(secretCode, null, 2)
            });
            
            if (!secretCode) {
              console.log('‚ö†Ô∏è [useSubscription] Code sans d√©tails, skip');
              continue;
            }
            
            const codeType = secretCode.type;
            const userExpiresAt = codeData.expires_at;
            
            console.log('üîë [useSubscription] Type de code:', codeType);
            console.log('üîë [useSubscription] Expire le:', userExpiresAt);
            console.log('üîë [useSubscription] Est actif:', secretCode.is_active);
            
            // üî• CORRECTION : Accepter 'lifetime' ET 'unlimited'
            if (codeType === 'lifetime' || codeType === 'unlimited') {
              hasActiveSecretCode = true;
              secretCodeType = codeType;
              secretCodeExpiresAt = null;
              console.log('‚úÖ [useSubscription] ========== CODE √Ä VIE/ILLIMIT√â TROUV√â ! ==========');
              console.log('‚úÖ [useSubscription] Type:', codeType);
              break;
            } else if (codeType === 'monthly') {
              if (!userExpiresAt || new Date(userExpiresAt) > new Date()) {
                hasActiveSecretCode = true;
                secretCodeType = codeType;
                secretCodeExpiresAt = userExpiresAt;
                console.log('‚úÖ [useSubscription] Code mensuel valide trouv√© !');
                break;
              } else {
                console.log('‚è∞ [useSubscription] Code mensuel expir√©');
              }
            }
          }
        } else {
          console.log('üì≠ [useSubscription] Aucun code secret trouv√©');
        }
      } catch (secretCodeError) {
        console.error('‚ùå [useSubscription] Erreur v√©rification codes:', secretCodeError);
      }

      console.log('üîë [useSubscription] ========== R√âSULTAT CODES ==========');
      console.log('üîë [useSubscription] hasActiveSecretCode:', hasActiveSecretCode);
      console.log('üîë [useSubscription] secretCodeType:', secretCodeType);

      // üî• √âTAPE 2 : V√©rifier l'abonnement Stripe seulement si pas de code secret
      let stripeSubscription = null;
      if (!hasActiveSecretCode) {
        console.log('üí≥ [useSubscription] ========== √âTAPE 2 : STRIPE ==========');
        try {
          const { data: customerData, error: customerError } = await supabase
            .from('stripe_customers')
            .select('customer_id')
            .eq('user_id', targetUserId)
            .maybeSingle();

          if (customerError) {
            console.error('‚ùå [useSubscription] Erreur customer:', customerError);
          } else if (customerData) {
            const { data: stripeData, error: stripeError } = await supabase
              .from('stripe_subscriptions')
              .select('*')
              .eq('customer_id', customerData.customer_id)
              .maybeSingle();

            if (stripeError) {
              console.error('‚ùå [useSubscription] Erreur subscription:', stripeError);
            } else {
              stripeSubscription = stripeData;
              console.log('üí≥ [useSubscription] Stripe subscription:', stripeSubscription?.status);
            }
          }
        } catch (stripeError) {
          console.error('‚ùå [useSubscription] Erreur Stripe:', stripeError);
        }
      } else {
        console.log('‚è≠Ô∏è [useSubscription] Code secret actif, skip Stripe');
      }

      // üî• √âTAPE 3 : D√©terminer le statut final
      const hasStripeAccess = stripeSubscription && 
        (stripeSubscription.status === 'active' || 
         stripeSubscription.status === 'trialing');
      
      const isSubscribed = hasStripeAccess || hasActiveSecretCode;

      const finalState = {
        isSubscribed,
        subscriptionStatus: stripeSubscription?.status || null,
        priceId: stripeSubscription?.price_id || null,
        currentPeriodEnd: stripeSubscription?.current_period_end || null,
        cancelAtPeriodEnd: stripeSubscription?.cancel_at_period_end || false,
        hasSecretCode: hasActiveSecretCode,
        secretCodeType,
        secretCodeExpiresAt,
        loading: false,
      };
      
      console.log('‚úÖ [useSubscription] ========== √âTAT FINAL ==========');
      console.log('‚úÖ [useSubscription] isSubscribed:', finalState.isSubscribed);
      console.log('‚úÖ [useSubscription] hasSecretCode:', finalState.hasSecretCode);
      console.log('‚úÖ [useSubscription] secretCodeType:', finalState.secretCodeType);
      console.log('‚úÖ [useSubscription] stripeStatus:', finalState.subscriptionStatus);
      console.log('‚úÖ [useSubscription] ========================================');
      
      setSubscription(finalState);

    } catch (error) {
      console.error('‚ùå [useSubscription] Erreur globale:', error);
      setSubscription({
        isSubscribed: false,
        subscriptionStatus: null,
        priceId: null,
        currentPeriodEnd: null,
        cancelAtPeriodEnd: false,
        hasSecretCode: false,
        secretCodeType: null,
        secretCodeExpiresAt: null,
        loading: false,
      });
    }
  };

  const refreshSubscription = () => {
    if (user) {
      fetchSubscription();
    }
  };

  return {
    ...subscription,
    refreshSubscription,
  };
};
